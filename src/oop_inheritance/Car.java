package oop_inheritance;

public class Car {
	int speed=100;
	
	public Car() {
		System.out.println("car--constructor");
	}
	public void start() {
		System.out.println("car---start");
		}
	public void stop() {
		System.out.println("car---stop");
	}
	public void refuel() {
		System.out.println("car---refuel");
	}
	public static void billing() {
		System.out.println("car--billing");
		
	}

}
